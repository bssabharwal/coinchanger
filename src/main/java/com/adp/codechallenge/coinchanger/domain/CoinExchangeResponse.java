package com.adp.codechallenge.coinchanger.domain;

public class CoinExchangeResponse {
	
	private Integer quarter;
	private Integer dime;
	private Integer nickel;
	private Integer penny;
	public Integer getQuarter() {
		return quarter;
	}
	public void setQuarter(Integer quarter) {
		this.quarter = quarter;
	}
	public Integer getDime() {
		return dime;
	}
	public void setDime(Integer dime) {
		this.dime = dime;
	}
	public Integer getNickel() {
		return nickel;
	}
	public void setNickel(Integer nickel) {
		this.nickel = nickel;
	}
	public Integer getPenny() {
		return penny;
	}
	public void setPenny(Integer penny) {
		this.penny = penny;
	}
	
	

}
