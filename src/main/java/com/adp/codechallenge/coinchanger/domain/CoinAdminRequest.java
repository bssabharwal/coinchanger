package com.adp.codechallenge.coinchanger.domain;

public class CoinAdminRequest {
	
	private int quarter;
	private int dime;
	private int nickel;
	private int penny;
	public int getQuarter() {
		return quarter;
	}
	public void setQuarter(int quarter) {
		this.quarter = quarter;
	}
	public int getDime() {
		return dime;
	}
	public void setDime(int dime) {
		this.dime = dime;
	}
	public int getNickel() {
		return nickel;
	}
	public void setNickel(int nickel) {
		this.nickel = nickel;
	}
	public int getPenny() {
		return penny;
	}
	public void setPenny(int penny) {
		this.penny = penny;
	}
	
	

}
